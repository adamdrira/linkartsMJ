

export function get_color_code(color: string) {
    
    if( color == "Noir" ) {
        return "rgba(59, 56, 56, 0.8)";
    }
    if( color == "Vert" ) {
        return "rgba(84, 130, 53, 0.8)";
    }
    if( color == "Jaune" ) {
        return "rgb(162, 158, 13, 0.85)";
    }
    if( color == "Rouge" ) {
        return "rgba(160, 0, 0, 0.8)";
    }
    if( color == "Violet" ) {
        return "rgba(148, 0, 148, 0.8)";
    }
    if( color == "Marron" ) {
        return "rgba(102, 51, 0, 0.8)";
    }
    if( color == "Orange" ) {
        return "rgb(253, 131, 4, 0.85)";
    }
    if( color == "Gris" ) {
        return "rgb(125, 125, 125, 0.85)";
    }
    
    return "rgba(47, 87, 151, 0.8)";  

}




